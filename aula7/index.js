// Váriavel constante
// Não podemos criar variáveis com palavras reservadas
// Variáveis precisam ter nomes significativos
// Não pode começar o nome de uima váriavel com um número <= Da erro de syntax
// Nome das váriaveis não podem conter espaços ou traços
// Utilizamos camelCase para fazer váriaveis com duas palavras
// Case-sensitive <- letras minusculas e maiusculas importam na hora de criar uma variável
// Não podemos modificar os valores de uma constante
// Constantes devem ser criadas e inicializadas com um valor

